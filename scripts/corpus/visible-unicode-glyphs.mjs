const glyphGridSize = 24;

export function visibleUnicodeText(x, y, size, value, { rtl = false } = {}) {
  const chars = [...value];
  if (rtl) {
    chars.reverse();
  }
  return chars
    .map((char, index) => vectorGlyph(x + index * size, y, size, char))
    .join("\n");
}

export function visibleVerticalUnicodeText(x, y, size, value) {
  return [...value]
    .map((char, index) => vectorGlyph(x, y - index * size, size, char))
    .join("\n");
}

function vectorGlyph(x, y, size, char) {
  const mask = glyphMasks[char];
  if (!mask) {
    throw new Error(`No visible corpus glyph mask for U+${char.codePointAt(0).toString(16).toUpperCase()}`);
  }
  const pixelSize = size / glyphGridSize;
  const rectangles = [];
  for (let row = 0; row < glyphGridSize; row += 1) {
    const bits = Number.parseInt(mask.slice(row * 6, row * 6 + 6), 16);
    let column = 0;
    while (column < glyphGridSize) {
      if ((bits & (1 << (glyphGridSize - column - 1))) === 0) {
        column += 1;
        continue;
      }
      const runStart = column;
      while (
        column < glyphGridSize &&
        (bits & (1 << (glyphGridSize - column - 1))) !== 0
      ) {
        column += 1;
      }
      rectangles.push(
        `${pdfNumber(x + runStart * pixelSize)} ${pdfNumber(y + (glyphGridSize - row - 1) * pixelSize)} ` +
          `${pdfNumber((column - runStart) * pixelSize)} ${pdfNumber(pixelSize)} re`
      );
    }
  }
  return `q 0 g\n${rectangles.join("\n")}\nf Q`;
}

function pdfNumber(value) {
  return Number(value.toFixed(3)).toString();
}
